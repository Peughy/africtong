  void chooseCategories(List<CategoriesModel> categories, int index) {
    for (int i = 0; i < categories.length; i++) {
      if (i != index) {
        if (categories[i].active) {
          categories[i].active = false;
          categories[index].active = true;
          break;
        }
      }
    }
  }

  String changeText(List<CategoriesModel> categories) {
    for (int i = 0; i < categories.length; i++) {
      if (categories[i].active) {
        return categories[i].categorie;
      }
    }
    return "";
  }
